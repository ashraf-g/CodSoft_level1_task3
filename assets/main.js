// JavaScript to fill progress bars as page loads
document.addEventListener("DOMContentLoaded", function() {
  var progressBars = document.querySelectorAll('.progress-bar');

  progressBars.forEach(function(progress) {
      var value = progress.getAttribute('aria-valuenow');
      var width = 0;
      var id = setInterval(frame, 10); // Adjust the interval for smoother animation

      function frame() {
          if (width >= value) {
              clearInterval(id);
          } else {
              width++;
              progress.style.width = width + '%';
          }
      }
  });
});
